// Memoized reverse dictionary
const reverseTranslations: Record<string, string> = Object.fromEntries(
  Object.entries(translations).map(([key, value]) => [value, key])
);

// Translate English -> Arabic
export const translateText = (text: string): string => {
  const cleaned = text.trim().toLowerCase();
  const match = Object.entries(translations).find(
    ([key]) => key.trim().toLowerCase() === cleaned
  );
  return match ? match[1] : text;
};

// Translate Arabic -> English
export const reverseTranslateText = (text: string): string => {
  return reverseTranslations[text.trim()] || text;
};